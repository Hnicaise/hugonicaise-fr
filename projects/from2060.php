        <h2>CONTEXT</h2>
        <p>As a group of 6 students, we had to create a online puzzle game. To win, you must anwser a series of riddles with an increasing dificulty. But to be able to solve them, you must use a game board on which are displayed clues and game missing informations. This hybrid game mixing digital and paper support gives you an immersive gameplay breaking the fourth wall.</p>
        <hr>
        <h2>SYNOPSIS</h2>
        <p>Your name is Isaac, a young boy about twenty, and you lost your memory. When you wake up, RosAI an artificial intelligence asks you help to accomplish a mission she cannot do alone. During this challenge, you will have to solve several riddles with her support.</p>
