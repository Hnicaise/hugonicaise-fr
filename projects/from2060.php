        <h2>CONTEXT</h2>
        <p>As a group of six students, we had to create an <strong>online puzzle game</strong>. To win, you must answer a series of riddles with an increasing difficulty. However, to be able to solve them, you must use a <strong>game board</strong> on which are displayed clues and game missing information. This hybrid game mixing <strong>digital and paper support</strong> gives you an immersing game-play breaking the <strong>fourth wall</strong>.</p>
        <hr>
        <h2>GAME SYNOPSIS</h2>
        <p>Your name is <strong>Isaac</strong>, a young boy about twenty, and you lost your memory. When you wake up, <strong>RosAI</strong> an artificial intelligence asks help to you to <strong>accomplish a mission</strong> she cannot do alone. During this adventure, you will have to solve several <strong>riddles</strong> with her support.</p>
