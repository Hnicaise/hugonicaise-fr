        <h2>GRAPHIC DESIGN</h2>
        <p>We took the decision to create the game in pixel art. This was on the one hand a way for us to stand out from the other students, on the other hand this kind of graphic design was really appropriate to gaming purpose. Indeed, pixel art makes you able to create complex illustrations with minimum effort, which was a good thing for us because we were time restricted. It also gives you this 8bit look.</p>
